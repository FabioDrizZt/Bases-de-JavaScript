// Ordenar un array

const numeros = [51, 22, 32, 41, 25, 39, 10]
// numeros.sort()
const numerosOrdenados = numeros.toSorted()
console.log(numeros)
console.log(numerosOrdenados)

// invertir el orden
// numerosOrdenados.reverse()
const numerosOrdenadosReversa = numerosOrdenados.toReversed()
console.log(numerosOrdenadosReversa)

